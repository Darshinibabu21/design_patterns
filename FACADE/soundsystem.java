package FACADE;

public class soundsystem {
    public void turnon()
    {
        System.out.println("soundsystem is turning on");
    }
    public void setvolume(int volume)
    {
        System.out.println("volume is set to " + volume);
    }
    public void turnoff()
    {
        System.out.println("soundsystem is turning off");
    }
}
